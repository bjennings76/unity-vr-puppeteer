  public static Color Lightened(this Color baseColor, float lightnessDelta) {
    HSLColor hslColor = baseColor;
    hslColor.l = Mathf.Clamp01(hslColor.l + lightnessDelta);
    return hslColor;
  }

  public static Color Darkened(this Color baseColor, float lightnessDelta) {
    HSLColor hslColor = baseColor;
    hslColor.l = Mathf.Clamp01(hslColor.l - lightnessDelta);
    return hslColor;
  }

  public static Color Modified(this Color baseColor, float hueDelta = 0.0f, float saturationDelta = 0.0f, float lightnessDelta = 0.0f, float alphaDelta = 0.0f) {
    HSLColor hslColor = baseColor;
    hslColor.h = Mathf.Repeat(hslColor.h + hueDelta, 360.0f);
    hslColor.s = Mathf.Clamp01(hslColor.s + saturationDelta);
    hslColor.l = Mathf.Clamp01(hslColor.l + lightnessDelta);
    hslColor.a = Mathf.Clamp01(hslColor.a + alphaDelta);
    return hslColor;
  }


