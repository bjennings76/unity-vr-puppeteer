  public static Color SetAlpha(this Color baseColor, float alpha) {
    Color color = baseColor;
    color.a = alpha;
    return color;
  }

  public static Color Faded(this Color baseColor, float alphaDelta) {
    Color color = baseColor;
    color.a = Mathf.Clamp01(color.a - alphaDelta);
    return color;
  }

  public static Color Saturated(this Color baseColor) {
    HSLColor hslColor = baseColor;
    hslColor.s = 1.0f;
    return hslColor;
  }

  public static Color Desaturated(this Color baseColor) {
    HSLColor hslColor = baseColor;
    hslColor.s = 0.0f;
    return hslColor;
  }


  public static Color Saturated(this Color baseColor, float saturationDelta) {
    HSLColor hslColor = baseColor;
    hslColor.s = Mathf.Clamp01(hslColor.s + saturationDelta);
    return hslColor;
  }

  public static Color Desaturated(this Color baseColor, float saturationDelta) {
    HSLColor hslColor = baseColor;
    hslColor.s = Mathf.Clamp01(hslColor.s - saturationDelta);
    return hslColor;
  }

  public static Color HueShifted(this Color baseColor, float hueDelta) {
    HSLColor hslColor = baseColor;
    hslColor.h = Mathf.Repeat(hslColor.h + hueDelta, 360.0f);
    return hslColor;
  }


  public static Color SetHue(this Color baseColor, float hue) {
    HSLColor hslColor = baseColor;
    hslColor.h = Mathf.Repeat(hue, 360.0f);
    return hslColor;
  }

  public static Color SetHue(this Color baseColor, Color hueColor) {
    HSLColor hslBaseColor = baseColor;
    HSLColor hslHueColor = hueColor;
    hslBaseColor.h = hslHueColor.h;
    return hslBaseColor;
  }


